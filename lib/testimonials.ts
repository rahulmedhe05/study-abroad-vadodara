// Get testimonials by area
export const getTestimonialsByArea = (area: string): Testimonial[] => {
  return testimonials.filter(t => t.area === area.toLowerCase().replace(/\s+/g, '-'));
};

// Get testimonials by country
export const getTestimonialsByCountry = (country: string): Testimonial[] => {
  return testimonials.filter(t => t.country.toLowerCase() === country.toLowerCase());
};

// Get random testimonials for general pages
export const getRandomTestimonials = (count: number = 6): Testimonial[] => {
  const shuffled = [...testimonials].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Get featured testimonials (high rating + diverse countries)
export const getFeaturedTestimonials = (): Testimonial[] => {
  const countries = ['Canada', 'USA', 'UK', 'Australia', 'Germany', 'Ireland'];
  return countries.map(country => 
    testimonials.find(t => t.country === country)!
  ).filter(Boolean);
};
